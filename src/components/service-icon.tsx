export type KnownServiceIcon = keyof typeof ICON_MAP;

export function ServiceIcon({
  name,
  className,
  ...props
}: { name: string; className?: string } & LucideProps) {
  const Cmp =
    ICON_MAP[name as KnownServiceIcon] ??
    HelpCircle;
  return <Cmp className={className} strokeWidth={1.75} {...props} />;
}
